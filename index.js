function add(a, b){
    let addition = a + b
    return addition;
}

function subtract(a, b){
    let subtraction = a - b
    return subtraction;
}

function multiply(a, b){
    let multiplication =  a * b
    return multiplication;
}

function divide(a, b){
    let division = a/b
    return division;
}

function increment(n){
    let incre = n+1
    return incre;
} 

function decrement (n){
    let decre = n-1
    return decre;
}

function makeInt(n){
    let convert = parseInt(n, 10);
    return convert;
}

function preserveDecimal(n){
    let convert2 = parseFloat(n)
    return convert2;
}